function Weapon.GetWeaponById(id) -- TODO Use key/value instead of array ?
	for _, weapon in pairs(weapons) do
		if weapon.id == id then return weapon end
	end

	return nil
end


function Weapon.GetMeleeWeapons()
	local meleeWeapons = { }

	for _, weapon in pairs(weapons) do
		if weapon.melee then
			table.insert(meleeWeapons, weapon)
		end
	end

	return meleeWeapons
end


function Weapon.GetPrimaryWeapons()
	local primaryWeapons = { }

	for _, weapon in pairs(weapons) do
		if weapon.primary then
			table.insert(primaryWeapons, weapon)
		end
	end

	return primaryWeapons
end


function Weapon.GetSecondaryWeapons()
	local secondaryWeapons = { }

	for _, weapon in pairs(weapons) do
		if weapon.secondary then
			table.insert(secondaryWeapons, weapon)
		end
	end

	return secondaryWeapons
end


function Weapon.GetGadgets()
	local gadgets = { }

	for _, weapon in pairs(weapons) do
		if weapon.gadget then
			table.insert(gadgets, weapon)
		end
	end

	return gadgets
end


function Weapon.GetSpawningAmmo(weaponHash)
	local clipSize = GetWeaponClipSize(weaponHash)
	if clipSize <= 0 then clipSize = 1 end
	return clipSize * Settings.weaponClipCount
end